// Naming rules for JS Variables
// Cannot start with a number
// Cannot have any hyphens/dashes
// Cannot be a keyword already

var myNickname = "Narciso \"Ciso\" Lobo";

// Primitive Datatypes - Strings, Numbers, Booleans, null, undefined
// Strings - strings are "strings" of characters

